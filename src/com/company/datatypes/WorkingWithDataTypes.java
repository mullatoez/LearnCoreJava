package com.company.datatypes;

public class WorkingWithDataTypes {
    public static void main(String[] args) {
        /*we have 8 primitive data types in Java*/
        byte age = 21; // -128 - 127
        short salary = 1000; // -32K - 32K
        int herAge = 25; // -2B - 2B 32bits
        long videoViews = 2_000_000_000L; // Very large number 64bits
        boolean isEligible = true;
        char myChar = 'A';
        double price = 10.99; //64 bits
        float pi = 31.02f; //32bits
    }
}
